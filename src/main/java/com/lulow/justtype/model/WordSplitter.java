package com.lulow.justtype.model;

/**
 * Splits a word string into a character array for per-character rendering.
 */
public class WordSplitter {

    /**
     * Converts a word into a char array.
     *
     * @param word the word to split
     * @return char array of the word
     */
    public char[] split(String word) {
        return word.toCharArray();
    }
}